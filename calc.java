package calc;


public class calc {
	double plus(double a, double b) {
		return a+b;
	}
	double minus(double a, double b) {
		return a-b;
	}
	double kobeitu(double a, double b) {
		return a*b;
	}
	double bolu(double a, double b) {
		return a/b;
	}
	double c (double a, double b) {
		return a;
	}
	double ac (double a, double b) {
		return 0;
	}
	double sqr (double a) {
		return a*a;
	}
	double sqrt (double a) {
		return Math.sqrt(a);
	}
	double perc (double a,double b) {
		return b*a/100;
	}
	
}
